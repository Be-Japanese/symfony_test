<?php

namespace App\Repository;

use App\Entity\Offer;
use Doctrine\Bundle\DoctrineBundle\Repository\ServiceEntityRepository;
use Doctrine\Persistence\ManagerRegistry;

/**
 * @extends ServiceEntityRepository<Offer>
 *
 * @method Offer|null find($id, $lockMode = null, $lockVersion = null)
 * @method Offer|null findOneBy(array $criteria, array $orderBy = null)
 * @method Offer[]    findAll()
 * @method Offer[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class OfferRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Offer::class);
    }

    public function save(Offer $entity, bool $flush = false): void
    {
        $this->getEntityManager()->persist($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function remove(Offer $entity, bool $flush = false): void
    {
        $this->getEntityManager()->remove($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function getOfferPaginatorQuery(): \Doctrine\ORM\QueryBuilder
    {

        return $this->createQueryBuilder('offer');
    }
    public function getOfferPaginatorQueryWithId(int $id): \Doctrine\ORM\Query
    {
        return $this->createQueryBuilder('o')
            ->innerJoin('o.player', 'p')
            ->innerJoin('p.team', 't')
            ->andWhere('t.id != :teamId')
            ->setParameter('teamId', $id)
            ->getQuery();

    }


    public function findOfferById($id)
    {
        $team = $this->getEntityManager()->getRepository(Offer::class)->find($id);

        if (!$team) {
            throw $this->createNotFoundException('Support Group does not exist');
        }

        return $team;
    }




